#While Loop
i = 1
while(i <= 5):
  print(i)
  i = i + 1
i = 1
while(i <= 5):
  print(i * "*")#It is Like Concatenation which Multiply String multiple times
  i = i + 1
i = 5
while(i >= 1):
  print(i * "*")
  i = i - 1
#For Loop (to iterate over a list)
for i in range(5):
  print(i)
  i = i + 1
for i in range(5):
  print(i * "*")
  i = i + 1